;THIS CODE CALCULATES SUM AND AVERAGE
;'R4' CONTAINS THE SUM
;'R3' CONTAINS THE AVERAGE	
	AREA RESET, CODE
		ENTRY
		MOV R3,#0
		MOV R4, #0
		LDR R0, =INPUTS
		LDR R1, =OUTPUTS
CONT
	LDR R2, [R0]
	ADD R4, R4, R2
	ADD R0, R0, #4
	ADD R3, R3, #1
	CMP R3, #5
	BNE CONT
	MOV R2, #5
	MOV R3, #0
	MOV R5,R4
REPT 
	SUBS R5, R5, R2
	ADDPL R3, R3, #1
	BPL REPT
	ADDMI R5, R5, R2
	STR R3, [R1]
STOP B STOP
INPUTS DCD 01,02,03,04,05
	AREA MEMORY, DATA
OUTPUTS SPACE 4
	END
